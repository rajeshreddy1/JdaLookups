package com.bedbath.jdalookups.model;

public class OptionsRes {
	
	private int ctlqs;
	private String desc;
	private String status;
	private float numVal;
	private String editcde;
	private String alphaVal;
	private int total;
	
	public int getCtlqs() {
		return ctlqs;
	}
	public void setCtlqs(int ctlqs) {
		this.ctlqs = ctlqs;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public float getNumVal() {
		return numVal;
	}
	public void setNumVal(float numVal) {
		this.numVal = numVal;
	}
	public String getEditcde() {
		return editcde;
	}
	public void setEditcde(String editcde) {
		this.editcde = editcde;
	}
	public String getAlphaVal() {
		return alphaVal;
	}
	public void setAlphaVal(String alphaVal) {
		this.alphaVal = alphaVal;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
}
