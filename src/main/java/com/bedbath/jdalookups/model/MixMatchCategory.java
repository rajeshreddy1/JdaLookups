package com.bedbath.jdalookups.model;

public class MixMatchCategory {

	private int mixMatchCategoryCode;
	private String mixMatchCategoryName;
	private Double totalRows;
	
	public int getMixMatchCategoryCode() {
		return mixMatchCategoryCode;
	}
	public void setMixMatchCategoryCode(int mixMatchCategoryCode) {
		this.mixMatchCategoryCode = mixMatchCategoryCode;
	}
	public String getMixMatchCategoryName() {
		return mixMatchCategoryName;
	}
	public void setMixMatchCategoryName(String mixMatchCategoryName) {
		this.mixMatchCategoryName = mixMatchCategoryName;
	}
	public Double getTotalRows() {
		return totalRows;
	}
	public void setTotalRows(Double totalRows) {
		this.totalRows = totalRows;
	}
	
}